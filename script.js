function converti(map) {
    let newMappa = {};

    for (let i in map) {
        newMappa[i] = {};
        newMappa[i].stanza = i.toString();
        newMappa[i].N = map[i][0];
        newMappa[i].S = map[i][1];
        newMappa[i].E = map[i][2];
        newMappa[i].O = map[i][3];
        newMappa[i].coin = 0;
        newMappa[i].vittoria = false;
    }
    return newMappa;
}


